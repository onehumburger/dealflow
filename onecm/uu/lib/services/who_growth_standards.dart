import 'dart:math' as math;

enum Gender { male, female }

enum MeasurementType { weight, height, headCircumference }

class _LMS {
  final double l;
  final double m;
  final double s;
  const _LMS(this.l, this.m, this.s);
}

/// WHO Growth Standards service providing LMS-based percentile calculations
/// for weight-for-age, height-for-age, and head-circumference-for-age
/// for boys and girls aged 0-36 months.
class WHOGrowthStandards {
  WHOGrowthStandards._();

  /// Calculate the percentile (0-100) for a given measurement.
  ///
  /// Uses the LMS method: Z = ((measurement/M)^L - 1) / (L*S) when L != 0
  /// or Z = ln(measurement/M) / S when L ≈ 0.
  /// Then converts Z-score to percentile via normal CDF.
  static double percentile({
    required double measurement,
    required int ageMonths,
    required Gender gender,
    required MeasurementType type,
  }) {
    assert(ageMonths >= 0 && ageMonths <= 36);
    final lms = _getLMS(gender, type, ageMonths);
    final z = _zScore(measurement, lms);
    return _normalCdf(z) * 100;
  }

  /// Returns percentile curves for charting.
  ///
  /// For each requested percentile value (e.g. 3, 15, 50, 85, 97),
  /// returns a list of (month, value) records from month 0 to 36.
  static Map<int, List<({int month, double value})>> getCurves({
    required Gender gender,
    required MeasurementType type,
    required List<int> percentiles,
  }) {
    final result = <int, List<({int month, double value})>>{};
    for (final p in percentiles) {
      final zTarget = _inverseNormalCdf(p / 100.0);
      final points = <({int month, double value})>[];
      for (int m = 0; m <= 36; m++) {
        final lms = _getLMS(gender, type, m);
        final value = _valueFromZ(zTarget, lms);
        points.add((month: m, value: value));
      }
      result[p] = points;
    }
    return result;
  }

  static _LMS _getLMS(Gender gender, MeasurementType type, int month) {
    final table = _tables[gender]![type]!;
    return table[month];
  }

  static double _zScore(double measurement, _LMS lms) {
    if (lms.l.abs() < 0.001) {
      return math.log(measurement / lms.m) / lms.s;
    }
    return (math.pow(measurement / lms.m, lms.l) - 1) / (lms.l * lms.s);
  }

  /// Convert a Z-score back to a measurement value.
  static double _valueFromZ(double z, _LMS lms) {
    if (lms.l.abs() < 0.001) {
      return lms.m * math.exp(lms.s * z);
    }
    return lms.m * math.pow(1 + lms.l * lms.s * z, 1 / lms.l);
  }

  /// Abramowitz & Stegun approximation for the standard normal CDF.
  static double _normalCdf(double z) {
    const a1 = 0.254829592;
    const a2 = -0.284496736;
    const a3 = 1.421413741;
    const a4 = -1.453152027;
    const a5 = 1.061405429;
    const p = 0.3275911;

    final sign = z < 0 ? -1 : 1;
    z = z.abs() / math.sqrt(2);

    final t = 1.0 / (1.0 + p * z);
    final y =
        1.0 - (((((a5 * t + a4) * t) + a3) * t + a2) * t + a1) * t * math.exp(-z * z);

    return 0.5 * (1.0 + sign * y);
  }

  /// Rational approximation for the inverse normal CDF (probit function).
  /// Uses the Beasley-Springer-Moro algorithm.
  static double _inverseNormalCdf(double p) {
    if (p <= 0) return -8.0;
    if (p >= 1) return 8.0;

    // Rational approximation for central region
    const a = [
      -3.969683028665376e+01,
      2.209460984245205e+02,
      -2.759285104469687e+02,
      1.383577518672690e+02,
      -3.066479806614716e+01,
      2.506628277459239e+00,
    ];
    const b = [
      -5.447609879822406e+01,
      1.615858368580409e+02,
      -1.556989798598866e+02,
      6.680131188771972e+01,
      -1.328068155288572e+01,
    ];
    const c = [
      -7.784894002430293e-03,
      -3.223964580411365e-01,
      -2.400758277161838e+00,
      -2.549732539343734e+00,
      4.374664141464968e+00,
      2.938163982698783e+00,
    ];
    const d = [
      7.784695709041462e-03,
      3.224671290700398e-01,
      2.445134137142996e+00,
      3.754408661907416e+00,
    ];

    const pLow = 0.02425;
    const pHigh = 1 - pLow;

    double q, r;

    if (p < pLow) {
      q = math.sqrt(-2 * math.log(p));
      return (((((c[0] * q + c[1]) * q + c[2]) * q + c[3]) * q + c[4]) * q + c[5]) /
          ((((d[0] * q + d[1]) * q + d[2]) * q + d[3]) * q + 1);
    } else if (p <= pHigh) {
      q = p - 0.5;
      r = q * q;
      return (((((a[0] * r + a[1]) * r + a[2]) * r + a[3]) * r + a[4]) * r + a[5]) *
          q /
          (((((b[0] * r + b[1]) * r + b[2]) * r + b[3]) * r + b[4]) * r + 1);
    } else {
      q = math.sqrt(-2 * math.log(1 - p));
      return -(((((c[0] * q + c[1]) * q + c[2]) * q + c[3]) * q + c[4]) * q + c[5]) /
          ((((d[0] * q + d[1]) * q + d[2]) * q + d[3]) * q + 1);
    }
  }

